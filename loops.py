#while loop
#increase
number =20
while number<=25:
    print(number)
    number+=1

#decrease
num =105
while num >= 100:
    print(num)
    num-=1

#for loop
for x in range(1,5):
    print(x)

for num in range(20,30,3):
    print(num)

#list
languages=["PHP","JAVA","PYTHON"]
for y in languages:
    print(y)