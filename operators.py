#Arithmetic operators

num1=50
num2=10

print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 % num2)

#comparison operators

print(num1 < num2)
print(num1 > num2)
print(num1 <= num2)
print(num1 >= num2)
print(num1 == num2)

print(num1 != num2)

#Assignment operators
x = 5
print(x)

x +=3
print(x)

y = 5
print(y)

y *=3
print(y)

#logic operators-and ,or,not
print(56 <100 and 78 >23 )
print(56 <100 or 78 < 23 )
print(not(56 <100 or 78 >23) )






